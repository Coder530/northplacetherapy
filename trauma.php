                <h2>What is trauma?</h2>
                <p>A useful definition of trauma is: an occurrence that threatens the survival of our physical or psychological integrity. Many people will experience this at some stage in their lives. Trauma can result from accidents, the loss of a loved one, witnessing or being a victim of a criminal act, or experiencing serious emotional or physical abuse. It can arise from events that expose us to intense fear, whether in a few moments or over a prolonged period. If these things happen, we may experience the events traumatically.</p>
