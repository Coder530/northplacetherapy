            <div class="section">
                <h2>Obsessive-Compulsive Disorder (OCD)</h2>
                <p>OCD is a mental health condition that involves persistent, unwanted thoughts (obsessions) and repetitive behaviors or mental rituals (compulsions) performed to reduce the anxiety caused by these thoughts. Many people with OCD recognize that their thoughts and behaviors are excessive, but feel unable to control them.</p>
                
                <h3>Common features of OCD</h3>
                <ul>
                    <li><strong>Obsessions:</strong> intrusive and distressing thoughts, images, or urges that are difficult to ignore. These often involve fears of harm, contamination, or moral concerns.</li>
                    <li><strong>Compulsions:</strong> repetitive behaviors or mental acts performed to reduce anxiety or prevent feared outcomes. These can include checking, washing, counting, or repeating actions.</li>
                </ul>
                
                <p>OCD can interfere significantly with daily life, making simple tasks feel overwhelming and consuming a great deal of time and energy.</p>
                
                <h3>Why therapy helps</h3>
                <p>Effective treatment for OCD usually involves therapy, particularly Cognitive Behavioral Therapy (CBT) with Exposure and Response Prevention (ERP). Therapy focuses on:</p>
                <ul>
                    <li>Understanding your obsessions and compulsions</li>
                    <li>Learning to tolerate the anxiety without performing compulsions</li>
                    <li>Developing strategies to reduce the impact of OCD on daily life</li>
                </ul>
                
                <p>OCD can feel isolating, but recovery is possible. Working with a trained therapist can help you regain control, reduce symptoms, and improve your quality of life.</p>
                
