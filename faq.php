                <div class="highlight-box">
                    <h3>A Note About Requests for Introductory Phone Calls</h3>
                    <p>I usually keep my phone on "do not disturb," so calls almost always go to voicemail. This helps me minimize disruptions during the day and focus on client sessions.</p>
                    <p>Email is generally the quickest way to get a response. You can read and reply at a convenient time—between sessions, during a walk, or over lunch. Voicemails, on the other hand, are usually checked at the end of the day, which can lead to delays in response, especially if the next day's diary is already full.</p>
                    <p>Many therapists offer short introductory calls, often 15–20 minutes. While it's fine to ask for one, I have chosen not to offer them in my practice. Instead, I offer full-length consultations for anyone considering therapy.</p>
                </div>

                <div class="faq-item">
                    <h3>Why I Don't Offer Introductory Calls</h3>
                    <ul>
                        <li><strong>Limited time for meaningful conversation:</strong> A short call rarely allows a potential client to explain complex issues, or for a therapist to understand whether they can provide helpful support.</li>
                        <li><strong>Emotional intensity:</strong> Initial calls often bring up highly personal or distressing material. A brief call may require interrupting someone mid-story or risk leaving them emotionally unsettled. A full consultation allows sufficient time to address difficult topics safely.</li>
                        <li><strong>Practical considerations:</strong> Even a short call requires scheduling, a private space, and diary management. Quick calls can disrupt sessions, administrative work, and personal time, especially when the practice is near capacity.</li>
                        <li><strong>Efficiency:</strong> Almost always, the outcome of an initial call is to arrange a full consultation anyway. Offering the consultation directly provides a clearer, more effective, and less stressful process for everyone involved.</li>
                    </ul>
                </div>

