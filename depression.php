            <h2>Depression Information</h2>
            <blockquote>
                "If you know someone who's depressed, please resolve never to ask them why. Depression isn't a straightforward response to a bad situation; depression just is, like the weather." – <em>Stephen Fry</em>
            </blockquote>
            
            <p>Depression is common. Studies show that up to 10% of people in England will experience depression in their lifetime. Mixed anxiety and depression is the most common mental health disorder in Britain, affecting around 8% of people clinically. Many people experience temporary low moods, but depression persists for weeks, months, or even longer.</p>
            
            <p>A diagnosis is usually made when deeper, longer-lasting periods of low mood and unhappiness are accompanied by a range of symptoms:</p>
            
            <h3>You may feel:</h3>
            <ul>
                <li>Worthless or hopeless</li>
                <li>That life isn't worth living</li>
                <li>Constantly anxious, tearful, or worried</li>
                <li>Unable to concentrate or make decisions</li>
                <li>Irritable or intolerant of others</li>
                <li>A lack of enjoyment in life</li>
                <li>Low self-esteem</li>
                <li>Excessive guilt</li>
                <li>No motivation or interest in activities you previously enjoyed</li>
            </ul>
            
            <h3>You may notice physical changes, such as:</h3>
            <ul>
                <li>Altered sleep patterns (insomnia or oversleeping)</li>
                <li>Changes in appetite (loss or overeating)</li>
                <li>Fatigue or low energy</li>
                <li>Persistent headaches, stomach issues, or chronic pain</li>
                <li>Slower speech</li>
                <li>Reduced libido or menstrual cycle changes</li>
            </ul>
            
            <h3>You may also:</h3>
            <ul>
                <li>Withdraw from hobbies and interests</li>
                <li>Isolate yourself from friends and family</li>
                <li>Reduce social participation</li>
                <li>Experience lower productivity at work</li>
            </ul>
            
            <p>Depression can develop gradually, and sometimes it takes a friend or family member to recognize it. Everyone experiences depression differently, both physically and psychologically. This is not a state of mind that can be "snapped out of"—a severe depressive episode can leave you housebound and struggling with even basic tasks.</p>
        </div>
        
        <div class="section">
            <h2>Severity and Causes</h2>
            <p>Depression is classified as a mood disorder and is typically described as mild, moderate, or severe:</p>
            <ul>
                <li><strong>Mild:</strong> Some impact on daily life</li>
                <li><strong>Moderate:</strong> Significant, noticeable impact</li>
                <li><strong>Severe:</strong> Functioning is almost impossible</li>
            </ul>
            
            <p>Causes are often complex, involving social, psychological, and biological factors. Life events such as bereavement, trauma, unemployment, or relationship breakdowns can trigger or exacerbate depression, creating a vicious cycle where symptoms lead to further stress and hopelessness. Biological factors, such as hormonal changes or low testosterone, can also play a role.</p>
        </div>
        
        <div class="section">
            <h2>Recovery and Support</h2>
            <p>Most people recover from depression, but it can persist or recur for some. It is important to keep your doctor informed, as antidepressants may be prescribed (see resources page for further information). Talking therapy has proven effectiveness in treating depression and reducing the risk of recurrence. The NHS typically offers short courses of CBT, though wait times can be long, and many people choose to see a private therapist.</p>
        </div>
        
        <div class="section">
            <h2>Steps You Can Take</h2>
            <p>Self-help strategies include:</p>
            <ul>
                <li>Accept that you are feeling this way and focus on maintaining basic self-care ("life hygiene")</li>
                <li>Talk to someone you trust about your feelings</li>
                <li>Keep active; even short daily walks can help mood and sleep</li>
                <li>Maintain a healthy diet</li>
                <li>Reduce alcohol, which may worsen depression</li>
                <li>Practice relaxing activities before sleep (reading, listening to music, TV)</li>
                <li>Identify and write down problems you can address, then take small, practical steps</li>
                <li>Stay hopeful—depression is common and treatable, and you can emerge stronger</li>
            </ul>
            
            <p>There are many helpful resources, such as Mind (see resources page). Stephen Fry's analogy of depression to "the weather" captures the sense of being overwhelmed by forces beyond your control. Depression can feel like a persistent low mood draining color and energy from life. Help is available, and small steps today can begin the recovery process.</p>
