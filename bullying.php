                <h2>Bullying</h2>
                <p>Bullying can occur in many areas of life, from the workplace and schools to online spaces and personal relationships. It can take many forms—verbal, physical, psychological, or digital—and often leaves long-lasting effects on self-esteem, confidence, and emotional wellbeing.</p>
                <h3>Understanding the Impact</h3>
                <p>Being bullied can lead to a range of emotional and physical responses, including anxiety, depression, irritability, sleep disturbances, and a diminished sense of self-worth. Even after bullying has stopped, the effects can linger, affecting relationships, work performance, and daily life.</p>
                <h3>Therapeutic Support</h3>
                <p>Therapy provides a safe space to process experiences of bullying, explore the impact on your self-esteem and confidence, and develop strategies to cope and rebuild. We work on practical approaches for setting boundaries, asserting yourself, and responding effectively in challenging situations. Over time, therapy can help you regain a sense of empowerment and resilience that bullying may have undermined.</p>
                <h3>For Workplace Bullying</h3>
                <p>In professional settings, bullying can involve harassment, intimidation, or unfair treatment. Therapy and coaching can help you navigate these dynamics, manage stress, and identify strategies for resolution or protection, while also supporting your emotional wellbeing.</p>
