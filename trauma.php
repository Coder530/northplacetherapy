            <div class="section">
                <h2>Trauma & Post-Traumatic Stress Disorder (PTSD)</h2>
                <p>Trauma is a response to events that are deeply distressing or threatening. This can include experiences such as accidents, assaults, abuse, natural disasters, or other situations where you felt unsafe or overwhelmed. For some, trauma can lead to post-traumatic stress disorder (PTSD), where the effects of the event continue to impact daily life long after it has passed.</p>
                
                <h3>Common features of trauma and PTSD include:</h3>
                <ul>
                    <li>Intrusive thoughts or flashbacks of the event</li>
                    <li>Nightmares or disturbed sleep</li>
                    <li>Strong emotional reactions or mood swings</li>
                    <li>Heightened alertness or startle responses</li>
                    <li>Avoidance of people, places, or activities that trigger memories</li>
                    <li>Feelings of guilt, shame, or self-blame</li>
                    <li>Difficulty concentrating or remembering things</li>
                    <li>Physical symptoms such as tension, headaches, or digestive issues</li>
                </ul>
                
                <p>Trauma affects both the mind and body. It can change how you perceive safety and trust, impacting relationships, work, and everyday functioning. PTSD develops when the natural process of recovering from a traumatic event is disrupted, often leading to persistent distress, hypervigilance, or emotional numbness.</p>
                
                <h3>Why therapy helps:</h3>
                <p>Therapy provides a safe, structured environment to process trauma, explore its impact, and begin to regain control over thoughts, emotions, and behaviours. Therapy can help you:</p>
                <ul>
                    <li>Understand and reframe traumatic memories</li>
                    <li>Reduce the intensity of intrusive thoughts and flashbacks</li>
                    <li>Learn strategies to manage stress and anxiety</li>
                    <li>Rebuild a sense of safety, trust, and confidence</li>
                </ul>
                
                <h3>Supporting yourself while recovering from trauma:</h3>
                <ul>
                    <li>Acknowledge your experience and allow yourself to feel your emotions without judgment</li>
                    <li>Maintain routines and small daily activities to restore a sense of stability</li>
                    <li>Practice grounding techniques such as focusing on your breath, body, or surroundings</li>
                    <li>Reach out to trusted friends, family, or support groups</li>
                    <li>Avoid self-blame—trauma is never your fault</li>
                </ul>
                
                <p>Recovery from trauma is possible, though it may take time. Therapy can support you in regaining a sense of safety, reducing distressing symptoms, and reconnecting with life.</p>
                
