            <h2>The Purpose of Anxiety</h2>
            <p>Emotions serve an evolutionary purpose—they help us respond to potential dangers. Anxiety, in particular, allows us to evaluate our environment and take urgent action if risks are detected. Problems arise when this natural mechanism becomes overly sensitive, leading to unnecessary suffering.</p>
        </div>
        
        <div class="section">
            <h2>Why Do I Have Anxiety?</h2>
            <p>Several factors usually contribute to anxiety:</p>
            <ul>
                <li>Genetics and physical factors</li>
                <li>Psychological makeup (how you think, feel, and behave)</li>
                <li>Environmental stressors</li>
            </ul>
            <p>Anxiety can be thought of as an emotional response to uncertainty. From childhood, we develop psychological "maps" to understand our environment and predict outcomes. In uncertain situations, these predictions become less reliable, triggering the brain's threat-detection systems and producing both physical and psychological anxiety symptoms.</p>
            
            <p>Sometimes, anxiety can create a self-reinforcing cycle, where we fear our own anxious feelings.</p>
            
            <p>Anxiety can present in various ways, including phobias, obsessive-compulsive disorder (OCD), situational or separation anxiety, and generalized anxiety disorder (GAD).</p>
        </div>
        
        <div class="section">
            <h2>Why Anxiety Seems More Common Today</h2>
            <p>Modern life is vastly different from that of previous generations. Technology and complex social environments require increasingly sophisticated mental "maps." Pressure to meet external expectations, often at odds with our inner needs, can lead to insecurity, uncertainty, and anxiety. Therapy is a supportive space to explore these pressures and regain balance.</p>
        </div>
        
        <div class="section">
            <h2>Common Symptoms</h2>
            <p>Anxiety can manifest physically, emotionally, and cognitively. Typical symptoms include:</p>
            <ul>
                <li>Excessive worry</li>
                <li>General feelings of unease</li>
                <li>Shakiness or dizziness</li>
                <li>Feeling faint or lightheaded</li>
                <li>Intrusive unpleasant thoughts</li>
                <li>Fear of "losing control" or impending doom</li>
                <li>Physical symptoms: increased heart rate, palpitations, shallow or rapid breathing, muscle tension, chest tightness, headaches, hot flushes, sweating, dry mouth, panic attacks</li>
            </ul>
            <p>Anxiety is common—about 1 in 10 adults may experience a disabling anxiety disorder at some point—but it is very treatable, and therapy can be highly effective.</p>
        </div>
        
