        <div class="section">
            <h2>Anxiety / Generalised Anxiety Disorder (GAD)</h2>
            <p>Anxiety is a common experience, and therapy can be very effective in managing it. This includes generalised anxiety disorder (GAD) as well as panic attacks.</p>
            
            <p>GAD involves excessive, uncontrollable worry about a variety of topics, often lasting six months or more. Anxiety often takes the form of "what if" thinking—worrying about things that might go wrong. This can be debilitating, as the mind jumps from one worry to another, affecting quality of life. Therapy provides a safe space to explore these patterns and begin making meaningful changes.</p>
            
            <h3>Common Features of Anxiety</h3>
            <ul>
                <li>Persistent worry or fear</li>
                <li>Feeling tense, restless, or on edge</li>
                <li>Difficulty concentrating or thinking clearly</li>
                <li>Physical symptoms such as rapid heartbeat, shallow breathing, dizziness, sweating, muscle tension, or headaches</li>
                <li>Panic attacks, which may combine several physical and emotional symptoms</li>
                <li>Avoidance of certain situations due to fear</li>
            </ul>
            
            <h3>Understanding Anxiety</h3>
            <p>Anxiety has a natural purpose—it helps us detect potential danger and respond quickly. Problems arise when this system becomes overactive, producing excessive worry that interferes with daily life. Anxiety is influenced by a combination of factors, including genetics, personality, thought patterns, life experiences, and environmental stressors.</p>
            
            <p>Anxiety can become a cycle: we fear uncertainty, which increases worry, which in turn heightens physical and emotional symptoms. Therapy can help break this cycle by addressing underlying thought patterns and learning practical coping strategies.</p>
            
            <h3>Supporting Yourself While Anxious</h3>
            <ul>
                <li>Recognise your anxiety without self-judgment</li>
                <li>Practice grounding techniques such as focusing on your breath, hands, and feet</li>
                <li>Take slow, deep breaths, allowing pauses between inhaling and exhaling</li>
                <li>Engage in regular activity and exercise</li>
                <li>Talk to someone you trust about your feelings</li>
                <li>Explore relaxation techniques such as mindfulness or meditation</li>
            </ul>
            <p>Anxiety is very common, and effective support is available. Therapy can reduce symptoms, help you understand triggers, and restore a sense of control over your thoughts, emotions, and body.</p>
            
