                <h2>The Impact of Abuse</h2>
                <p>Domestic abuse can affect physical health, emotional wellbeing, and social relationships. Victims may experience:</p>
                <ul>
                    <li>Anxiety, depression, or post-traumatic stress</li>
                    <li>Low self-esteem or feelings of worthlessness</li>
                    <li>Physical injuries or chronic health issues</li>
                    <li>Social withdrawal and isolation</li>
                    <li>Difficulty making decisions or trusting others</li>
                </ul>
                <p>Children and young people exposed to abuse may also experience emotional or behavioral difficulties.</p>
