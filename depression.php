            <h2>Depression (Summary)</h2>
            <p>Depression is a common experience, but it is very treatable with therapy. Symptoms often include low mood most of the day, nearly every day, and a loss of interest or pleasure in activities, typically lasting at least two weeks. Other signs may include changes in appetite, weight, sleep, or concentration, as well as feelings of restlessness, irritability, or lethargy. Psychologically, depression can involve feelings of worthlessness or frequent thoughts about death.</p>
            
            <p>Depression can also affect motivation, making even simple tasks—like leaving the house—feel difficult. It can impact how you see yourself and assumptions about how others perceive you.</p>
            
            <p>Therapy is a proven starting point. Talking through the issues contributing to depression, and beginning to make positive changes, can help you feel better and regain control over your life.</p>
            
            <h3>Common symptoms of depression include:</h3>
            <ul>
                <li>Persistent low mood or sadness</li>
                <li>Loss of interest or pleasure in usual activities</li>
                <li>Changes in sleep or appetite</li>
                <li>Fatigue or loss of energy</li>
                <li>Difficulty concentrating or making decisions</li>
                <li>Feelings of worthlessness or excessive guilt</li>
                <li>Irritability, anxiety, or tearfulness</li>
                <li>Social withdrawal or neglecting hobbies</li>
                <li>Physical complaints such as headaches, stomach issues, or chronic pain</li>
            </ul>
            
            <p>Depression varies in severity: mild, moderate, or severe, depending on the impact on daily life. Causes are often complex, involving social, psychological, and biological factors. Difficult life events—such as bereavement, trauma, relationship breakdowns, or unemployment—can trigger or worsen depressive episodes. Biological factors, like hormonal changes, can also contribute.</p>
            
            <h3>Supporting yourself while dealing with depression:</h3>
            <ul>
                <li>Accept your current feelings without self-judgment</li>
                <li>Maintain good daily routines, including activity and sleep</li>
                <li>Talk to someone you trust about how you feel</li>
                <li>Eat well and limit alcohol</li>
                <li>Engage in small daily activities or light exercise</li>
                <li>Consider writing down challenges and possible solutions</li>
                <li>Hold onto hope—recovery is possible, and therapy can accelerate the process</li>
            </ul>
            
            <p>Therapy can help reduce the intensity of symptoms, explore underlying causes, and develop strategies to prevent recurrence. For immediate support or if symptoms are severe, contact your GP or local mental health services.</p>
            
