                <p>Domestic abuse is about power and control. It may involve intimidation, coercion, threats, manipulation, or isolation from friends and family. Even subtle forms of emotional or psychological abuse can have a significant impact, leaving the victim feeling anxious, depressed, or unable to trust their own perceptions.</p>
                <p>It is important to remember that abuse is never the fault of the person being harmed. Everyone deserves safety, respect, and dignity in their relationships.</p>
